package main.model.handleBarComponents;

import main.model.ComponentPart;

public class HandleBarComponents {
	
	private ComponentPart handleBarGrip;
	private ComponentPart headTube;
	private ComponentPart shockAbsorber;
	private ComponentPart brake;
	private ComponentPart fork;
	private ComponentPart gearControl;
	
	public ComponentPart getHandleBarGrip() {
		return handleBarGrip;
	}
	public void setHandleBarGrip(ComponentPart handleBarGrip) {
		this.handleBarGrip = handleBarGrip;
	}
	public ComponentPart getHeadTube() {
		return headTube;
	}
	public void setHeadTube(ComponentPart headTube) {
		this.headTube = headTube;
	}
	public ComponentPart getShockAbsorber() {
		return shockAbsorber;
	}
	public void setShockAbsorber(ComponentPart shockAbsorber) {
		this.shockAbsorber = shockAbsorber;
	}
	public ComponentPart getBrake() {
		return brake;
	}
	public void setBrake(ComponentPart brake) {
		this.brake = brake;
	}
	public ComponentPart getFork() {
		return fork;
	}
	public void setFork(ComponentPart fork) {
		this.fork = fork;
	}
	public ComponentPart getGearControl() {
		return gearControl;
	}
	public void setGearControl(ComponentPart gearControl) {
		this.gearControl = gearControl;
	}
	
	
}
